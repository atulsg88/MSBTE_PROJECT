package com.epayment.Bean;

public class InstituteBean {

	private int institute_id;
	private String institute_name;
	private String institute_code;
	private String email;
	private String mobile;
	private String username;
	private String password;
	private String address;
	private String registerd_date;
	private String institute_status;
	public int getInstitute_id() {
		return institute_id;
	}
	public void setInstitute_id(int institute_id) {
		this.institute_id = institute_id;
	}
	public String getInstitute_name() {
		return institute_name;
	}
	public void setInstitute_name(String institute_name) {
		this.institute_name = institute_name;
	}
	public String getInstitute_code() {
		return institute_code;
	}
	public void setInstitute_code(String institute_code) {
		this.institute_code = institute_code;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getRegisterd_date() {
		return registerd_date;
	}
	public void setRegisterd_date(String registerd_date) {
		this.registerd_date = registerd_date;
	}
	public String getInstitute_status() {
		return institute_status;
	}
	public void setInstitute_status(String institute_status) {
		this.institute_status = institute_status;
	}
	


}
